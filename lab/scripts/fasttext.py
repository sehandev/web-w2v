중성 = ['ㅏ', 'ㅐ', 'ㅑ', 'ㅒ', 'ㅓ', 'ㅔ', 'ㅕ', 'ㅖ', 'ㅗ', 'ㅘ', 'ㅙ', 'ㅚ', 'ㅛ', 'ㅜ', 'ㅝ', 'ㅞ', 'ㅟ', 'ㅠ', 'ㅡ', 'ㅢ', 'ㅣ']
종성 = ['$', 'ㄱ', 'ㄲ', 'ㄳ', 'ㄴ', 'ㄵ', 'ㄶ', 'ㄷ', 'ㄹ', 'ㄺ', 'ㄻ', 'ㄼ', 'ㄽ', 'ㄾ', 'ㄿ', 'ㅀ', 'ㅁ', 'ㅂ', 'ㅄ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ']


def split_jamo(word):
    # 단어를 받아서 한글만 자모분리

    result_list = ['<']  # 시작
    for ch in list(word.strip()):
        if '가' <= ch <= '힣':
            tmp = ord(ch) - ord('가')
            aa = tmp // 588
            bb = (tmp - (588 * aa)) // 28
            cc = tmp - (588 * aa) - (28 * bb)
            result_list.extend([초성[aa], 중성[bb], 종성[cc]])
        else:
            result_list.append(ch)
    result_list.append('>')  # 끝

    return result_list


